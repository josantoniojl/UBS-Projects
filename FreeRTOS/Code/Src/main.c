/**
* @brief Function implementing the I2C thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_I2Communication */
void I2Communication(void const * argument)
{
  /* USER CODE BEGIN I2Communication */

	TickType_t xLastWakeTime;
	xLastWakeTime = xTaskGetTickCount(); /* La variable xLastWakeTime doit être initialisée avec le nombre de ticks courant. C'est la seule fois où la variable est écrite explicitement. Après cela, xLastWakeTime est automatiquement mis à jour dans vTaskDelayUntil(). */

  /* Infinite loop */
  for(;;)
  {
		HAL_I2C_Mem_Read (&hi2c1, SLAVEI2C_ADD,0x28, 1,(uint8_t *)OUT_X_L, 1, 100); // Registre OUT_X_L
		HAL_I2C_Mem_Read (&hi2c1, SLAVEI2C_ADD,0x29, 1,(uint8_t *)OUT_X_H, 1, 100); // Registre OUT_X_H
		HAL_I2C_Mem_Read (&hi2c1, SLAVEI2C_ADD,0x2A, 1,(uint8_t *)OUT_Y_L, 1, 100); // Registre OUT_Y_L
		HAL_I2C_Mem_Read (&hi2c1, SLAVEI2C_ADD,0x2B, 1,(uint8_t *)OUT_Y_H, 1, 100); // Registre OUT_Y_H
		HAL_I2C_Mem_Read (&hi2c1, SLAVEI2C_ADD,0x2C, 1,(uint8_t *)OUT_Z_L, 1, 100); // Registre OUT_Z_L
		HAL_I2C_Mem_Read (&hi2c1, SLAVEI2C_ADD,0x2D, 1,(uint8_t *)OUT_Z_H, 1, 100); // Registre OUT_Z_H
		x = ((OUT_X_H[0]<<8)|OUT_X_L[0]); // Integration des deux complements
		y = ((OUT_Y_H[0]<<8)|OUT_Y_L[0]); // Integration des deux complements
		z = ((OUT_Z_H[0]<<8)|OUT_Z_L[0]); // Integration des deux complements
		
		xSemaphoreGive(signal);
		
		HAL_UART_Transmit(&huart2, (uint8_t*)acelerations, sprintf(acelerations, "%d,%d,%d\n", x,y,z),10);
		/* Cette tâche doit être exécutée toutes les 150 millisecondes exactement. Selon la fonction vTaskDelay(), le temps est mesuré en ticks, et la macro pdMS_TO_TICKS() est utilisée pour convertir les millisecondes en ticks. xLastWakeTime est automatiquement mis à jour dans vTaskDelayUntil(), n'est donc pas explicitement mis à jour par la tâche. */
    vTaskDelayUntil( &xLastWakeTime, pdMS_TO_TICKS( 150 ) );
  }
  /* USER CODE END I2Communication */
}

/* USER CODE BEGIN Header_PWM */
/**
* @brief Function implementing the PWMCONTROL thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_PWM */
void PWM(void const * argument)
{
  /* USER CODE BEGIN PWM */
  /* Infinite loop */
  for(;;)
  {
		if (xSemaphoreTake(signal,portMAX_DELAY))
			{
				xa=x*.0000591;
				ya=y*.0000589;
				za=z*.0000522; //110


				x_pwm=150+xa*100;
				y_pwm=130+ya*85;
				z_pwm=130+za*90; //110
				
				if(x_pwm<50)
					x_pwm=50;
				else if(x_pwm>250)
					x_pwm=250;
				
				if(y_pwm<45)
					y_pwm=45;
				else if(y_pwm>215)
					y_pwm=215;
				
				if(z_pwm<130)
					z_pwm=130;
				else if(z_pwm>220)
					z_pwm=220;	
				
				TIM2->CCR1=x_pwm;
				TIM2->CCR2=y_pwm;
				TIM2->CCR4=z_pwm;
		}
		osDelay(10);
  }
  /* USER CODE END PWM */
}

