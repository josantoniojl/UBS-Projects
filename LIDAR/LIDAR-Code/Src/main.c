/* USER CODE BEGIN 0 */
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) 
	{
		if(htim -> Instance == TIM2) 
			{// Si CH1 est en haut ou en bas
				if(htim -> Channel == HAL_TIM_ACTIVE_CHANNEL_1) 
					{ // Si l'entrée de PWM capture est en haut
						if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0)==1) 
							{ 
								TIM2 -> CCER |= TIM_CCER_CC1P;  // polarité de comparaison de sortie sur Bas 
								TIM2 -> CNT = 0; // Reset du compteur
							}
						else
							{
								IC1Valeur = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1); // Lecture de la valeur de CH1
								TIM2 -> CCER &= ~TIM_CCER_CC1P; // polarité de comparaison de sortie sur Haute 
								Largeur = IC1Valeur;  // Largeur du signal
								Distance=Largeur/10;   //On divise la signal sur 10 afin d'obtenir la valeur en cm
								VPWM=(1800*Distance)/DistanceMax; //On modifie la distance maximal pour avoir un rapport du 100
								/**** Condition pour ne pas depasser la valeur PWM*/
								if(VPWM>1800)
									VPWM2=1800;
								else
									VPWM2=VPWM;
					
								TIM1->CCR1=VPWM2; //La valeur est asigné afin d'avoir la PWM
							}		
					}
			}
	}
/* USER CODE END 0 */

